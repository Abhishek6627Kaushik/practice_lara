//Java Program to Swap Two Numbers
package programiz_example;
import java.util.*;
public class SwapTwoNum
{
	 public static void main(String[] args) 
	 {
		Scanner sc  = new Scanner (System.in);
		 int  n1 =  sc.nextInt();
		 int n2 = sc .nextInt();
		 
		 int first =  n2 ;
		 int second  = n1;
		 System.out.println("After Swaping "+ first +" and "+second);
		 
	 }
}
